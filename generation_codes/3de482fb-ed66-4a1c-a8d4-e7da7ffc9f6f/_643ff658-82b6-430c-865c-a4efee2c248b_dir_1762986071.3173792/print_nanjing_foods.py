# 功能：规整打印南京美食分类和项目

# 定义美食分类和数据
foods = {
    "经典必吃榜": ["金陵盐水鸭", "鸭血粉丝汤", "牛肉锅贴", "小笼包/汤包"],
    "特色小吃榜": ["活珠子/旺鸡蛋", "梅花糕", "糖芋苗", "赤豆元宵", "南京皮肚面"],
    "甜品 & 饮品": ["美龄粥", "雨花茶"],
    "美食街区推荐": ["老门东", "科巷", "明瓦廊", "夫子庙", "狮子桥美食街"]
}

# 打印所有美食分类和项目
for category, items in foods.items():
    print(f"{category}:")
    for item in items:
        print(f"  - {item}")
    print()  # 空行分隔不同分类